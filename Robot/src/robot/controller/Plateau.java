package robot.controller;

public class Plateau {

	private int totalX;
	private int totalY;

	public Plateau(int totalX, int totalY) {
		this.totalX = totalX;
		this.totalY = totalY;
	}
	
	public int getTotalX() {
		return totalX;
	}

	public int getTotalY() {
		return totalY;
	}

}
